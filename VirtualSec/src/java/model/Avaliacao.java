package model;

/**
 *
 * @author aldo_neto
 */
public class Avaliacao {
    private int comportamento;
    private int aptidao;
    private int participacao;
    private int trabEquipe;
    private int lideranca;

    /**
     * @return the comportamento
     */
    public int getComportamento() {
        return comportamento;
    }

    /**
     * @param comportamento the comportamento to set
     */
    public void setComportamento(int comportamento) {
        this.comportamento = comportamento;
    }

    /**
     * @return the aptidao
     */
    public int getAptidao() {
        return aptidao;
    }

    /**
     * @param aptidao the aptidao to set
     */
    public void setAptidao(int aptidao) {
        this.aptidao = aptidao;
    }

    /**
     * @return the participacao
     */
    public int getParticipacao() {
        return participacao;
    }

    /**
     * @param participacao the participacao to set
     */
    public void setParticipacao(int participacao) {
        this.participacao = participacao;
    }

    /**
     * @return the trabEquipe
     */
    public int getTrabEquipe() {
        return trabEquipe;
    }

    /**
     * @param trabEquipe the trabEquipe to set
     */
    public void setTrabEquipe(int trabEquipe) {
        this.trabEquipe = trabEquipe;
    }

    /**
     * @return the lideranca
     */
    public int getLideranca() {
        return lideranca;
    }

    /**
     * @param lideranca the lideranca to set
     */
    public void setLideranca(int lideranca) {
        this.lideranca = lideranca;
    }
    
}
