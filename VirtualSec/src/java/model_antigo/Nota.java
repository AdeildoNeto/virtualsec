public class Nota {
    
    private float lideranca;
    private float trabEquipe;
    private float aptidao;
    private float comportamento;
    private float participacao;

    /**
     * @return the lideranca
     */
    public float getLideranca() {
        return lideranca;
    }

    /**
     * @param lideranca the lideranca to set
     */
    public void setLideranca(float lideranca) {
        this.lideranca = lideranca;
    }

    /**
     * @return the trabEquipe
     */
    public float getTrabEquipe() {
        return trabEquipe;
    }

    /**
     * @param trabEquipe the trabEquipe to set
     */
    public void setTrabEquipe(float trabEquipe) {
        this.trabEquipe = trabEquipe;
    }

    /**
     * @return the aptidao
     */
    public float getAptidao() {
        return aptidao;
    }

    /**
     * @param aptidao the aptidao to set
     */
    public void setAptidao(float aptidao) {
        this.aptidao = aptidao;
    }

    /**
     * @return the comportamento
     */
    public float getComportamento() {
        return comportamento;
    }

    /**
     * @param comportamento the comportamento to set
     */
    public void setComportamento(float comportamento) {
        this.comportamento = comportamento;
    }

    /**
     * @return the participacao
     */
    public float getParticipacao() {
        return participacao;
    }

    /**
     * @param participacao the participacao to set
     */
    public void setParticipacao(float participacao) {
        this.participacao = participacao;
    }
    
    
}
